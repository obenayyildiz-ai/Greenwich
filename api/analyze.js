export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  
  if (req.method === 'OPTIONS') {
    return res.status(200).end();
  }
  
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }
  
  try {
    const { prompt } = req.body;
    
    if (!prompt) {
      return res.status(400).json({ error: 'Prompt manquant' });
    }
    
    const apiKey = process.env.ANTHROPIC_API_KEY;
    
    if (!apiKey) {
      console.error('ANTHROPIC_API_KEY non definie');
      return res.status(500).json({ 
        error: 'Configuration serveur incorrecte - cle API manquante'
      });
    }
    
    console.log('Debut analyse, longueur prompt:', prompt.length);
    
    const Anthropic = (await import('@anthropic-ai/sdk')).default;
    const anthropic = new Anthropic({ apiKey });
    
    console.log('Appel API Anthropic...');
    
    const response = await anthropic.messages.create({
      model: 'claude-sonnet-4-20250514',
      max_tokens: 3000,
      messages: [
        {
          role: 'user',
          content: prompt,
        },
      ],
    });
    
    console.log('Reponse recue');
    
    return res.status(200).json({
      content: response.content,
    });
    
  } catch (error) {
    console.error('Erreur:', error.message);
    console.error('Type:', error.constructor.name);
    console.error('Code:', error.status || error.code);
    
    return res.status(500).json({ 
      error: error.message,
      type: error.constructor.name,
      code: error.status || error.code
    });
  }
}
